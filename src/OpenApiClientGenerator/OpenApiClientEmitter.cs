    private sealed class CodeWriter
    {
        private readonly StringBuilder _builder = new StringBuilder();
        private int _indentLevel;

        public void Indent()
        {
            _indentLevel++;
        }

        public void Unindent()
        {
            _indentLevel--;
        }

        public void WriteLine(string value = "")
        {
            if (value.Length == 0)
            {
                _builder.AppendLine();
                return;
            }

            _builder.Append(new string(' ', _indentLevel * 4));
            _builder.AppendLine(value);
        }

        public void TrimTrailingBlankLine()
        {
            while (_builder.Length > 0 && (_builder[_builder.Length - 1] == '\r' || _builder[_builder.Length - 1] == '\n'))
            {
                _builder.Length--;
            }

            if (_builder.Length == 0)
            {
                return;
            }

            var lastLineFeed = -1;
            for (var index = _builder.Length - 1; index >= 0; index--)
            {
                if (_builder[index] == '\n')
                {
                    lastLineFeed = index;
                    break;
                }
            }

            if (lastLineFeed < 0)
            {
                return;
            }

            _builder.AppendLine();
        }

        public override string ToString()
        {
            return _builder.ToString();
        }
    }
